char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char			*dest_c;
	unsigned int	i;

	i = 0;
	dest_c = dest;
	while (*src && i < n)
	{
		*dest = *src;
		dest++;
		src++;
		i++;
	}
	while (i < n)
	{
		*dest = '\0';
		dest++;
		i++;
	}
	return (dest_c);
}
