#include <unistd.h>

int	main(int i, char **args)
{
	int	j;

	if (i == 0)
		return (1);
	j = 0;
	while (args[0][j])
		write(1, &args[0][j++], 1);
	write(1, "\n", 1);
	return (0);
}
