#include <unistd.h>

int	main(int i, char **args)
{
	int	j;
	int	k;

	j = 1;
	k = 0;
	while (j < i)
	{
		while (args[j][k])
		{
			write(1, &args[j][k], 1);
			k++;
		}
		k = 0;
		write (1, "\n", 1);
		j++;
	}
	return (0);
}
